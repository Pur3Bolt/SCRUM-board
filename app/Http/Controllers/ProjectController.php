    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Project  $project
     * @return \Illuminate\Http\Response
     */
    public function show(Project $project)
    {
        Project::findOrFail($project->id);
        $this->authorize('view', [Project::class, $project]);
        $stories = DB::select("SELECT * from stories WHERE project_id={$project->id} AND priority != 4");
        $stories_wont_have = DB::select("SELECT * from stories WHERE project_id={$project->id} AND priority = 4");
        $sprints = DB::select("SELECT * from sprints WHERE project_id={$project->id} ORDER BY start_date ASC");
        return view('project.show', ['stories' => $stories, 'stories_wont_have' => $stories_wont_have, 'project' => $project, 'sprints' => $sprints]);
    }

